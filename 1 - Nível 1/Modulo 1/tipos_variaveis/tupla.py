#Criando uma tupla de exemplo
minha_tupla = (1,2,2,3,4)

print(f"Minha tupla: {minha_tupla}")

print(f"minha_tipla[0]: {minha_tupla[0]}")
print(f"minha_tipla[0]: {minha_tupla[0]}")
print(f"minha_tipla[0]: {minha_tupla[-1]}")

#Metodo count
contagem = minha_tupla.count(2)
print(f"Quantidade de vezes que o elemento 2 aparace: {contagem}")

indice = minha_tupla.index(3)
print(f"Indice da primetira ocorrência do número 3: {indice} posição")